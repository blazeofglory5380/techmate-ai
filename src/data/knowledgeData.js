export function normalizeKnowledgeRecord(item) {
  return {
    id: item.id,
    title: item.title || "Untitled knowledge record",
    category: item.category || "Troubleshooting",
    sourceType: item.sourceType || "Note",
    tags: normalizeList(item.tags),
    summary: item.summary || "",
    skillLevel: item.skillLevel || "Beginner",
    relatedVendor: item.relatedVendor || item.vendor || "None",
    relatedTopics: normalizeList(item.relatedTopics),
    dateAdded: item.dateAdded || new Date().toISOString().slice(0, 10),
    relatedCommands: normalizeList(item.relatedCommands),
    relatedErrors: normalizeList(item.relatedErrors),
    relatedPlaybooks: normalizeList(item.relatedPlaybooks || item.troubleshootingSteps),
    troubleshootingSteps: normalizeList(item.troubleshootingSteps || item.relatedPlaybooks),
    notes: item.notes || "",
    fileName: item.fileName || "",
    fileType: item.fileType || "",
  };
}

export function knowledgeSearchText(item) {
  return [
    item.title,
    item.category,
    item.summary,
    item.notes,
    item.sourceType,
    item.skillLevel,
    item.relatedVendor,
    ...normalizeList(item.tags),
    ...normalizeList(item.relatedTopics),
    ...normalizeList(item.relatedCommands),
    ...normalizeList(item.relatedErrors),
    ...normalizeList(item.relatedPlaybooks),
  ]
    .join(" ")
    .toLowerCase();
}

function normalizeList(value) {
  if (Array.isArray(value)) return value.filter(Boolean);
  return String(value || "")
    .split(",")
    .map((item) => item.trim())
    .filter(Boolean);
}
